class Solution {
public:
    int maxDepth(Node* root) {
        return getDepth(root);

    }
    int getDepth(Node* node )
    {
        if(node==nullptr)return 0;
           int maxDepth = 0;
        for (auto & node_i : node->children)
        {
            maxDepth= max(maxDepth,getDepth(node_i));
        }
        return 1+maxDepth;
    }
};
